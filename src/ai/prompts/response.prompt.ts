export const buildChatPrompt = (
  userMessage: string,
  category?: string,
  serviceName?: string,
): string => {
  let context = '';

  if (category) {
    context += `\n[المشكلة مصنفة كـ: ${category}]`;
  }
  if (serviceName) {
    context += `\n[الخدمة المقترحة: ${serviceName}]`;
  }

  return `${userMessage}${context}`;
};